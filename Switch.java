public class Switch {
    public static void main(String[] args) {
        int a = 1;

        switch (a) {
            case 1:
                System.out.println("You have Choosen 1");
                break;

            case 2:
                System.out.println("You have Choosen 2");
                break;
            default:
                System.out.println("Choose the Valid Option");
                break;
        }
    }

}
