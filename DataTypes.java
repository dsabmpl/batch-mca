public strictfp class DataTypes {
    public static void main(String[] args) {
        byte age = 22; // -128 to 127 // byte literal
        long e = 1000000000L; // long literal
        // int a; // Non - Decimal
        // char b;
        // double t = 20.90; // Decimal
        int x = 10;
        System.out.println(x + 10); // 20
        System.out.println(x); // 10
        float price = 90.20f; // F or f - Float Literal
        double amount = 1000000.8882;
        char accept = 'рди';
        System.out.println(accept);
        boolean att = true;
    }
}
